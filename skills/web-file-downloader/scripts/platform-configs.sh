# 平台配置示例
# 复制并修改这些配置来适配不同的目标平台

## 太极平台 (a.taiji.woa.com)
export TAIJI_URL="a.taiji.woa.com"
export TAIJI_ANCHOR="配置文件"
export TAIJI_SIDEBAR=".ant-drawer"
export TAIJI_FILE_BTN="文件管理"
export TAIJI_TABLE=".ant-modal table"
export TAIJI_OP_INDEX=0

